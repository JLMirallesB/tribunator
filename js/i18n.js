Tribunator.i18n = {
  currentLang: 'es',

  init: function() {
    var saved = localStorage.getItem('tribunator_language');
    if (saved && Tribunator.translations[saved]) {
      this.currentLang = saved;
    }
  },

  setLanguage: function(lang) {
    if (Tribunator.translations[lang]) {
      this.currentLang = lang;
      localStorage.setItem('tribunator_language', lang);
    }
  },

  t: function(key) {
    var parts = key.split('.');
    var obj = Tribunator.translations[this.currentLang];
    for (var i = 0; i < parts.length; i++) {
      if (obj && obj[parts[i]] !== undefined) {
        obj = obj[parts[i]];
      } else {
        var fallback = Tribunator.translations['es'];
        for (var j = 0; j < parts.length; j++) {
          if (fallback && fallback[parts[j]] !== undefined) {
            fallback = fallback[parts[j]];
          } else {
            return key;
          }
        }
        return fallback;
      }
    }
    return obj;
  }
};

function t(key) {
  return Tribunator.i18n.t(key);
}
